import React, { useEffect, useState } from 'react';
import axios from 'axios';
import DOMPurify from 'dompurify';
import './CategoryPost.css';
import { useNavigate } from 'react-router-dom';
import HomeLayoutAlternative from '../../Layout/HomeLayoutAlternative';
import LatestNews from '../../Components/LatestNews';
import Http from '../../Http';

const CategoryPost = ({catName}) => {
  const [forms, setForms] = useState([]);
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState(null);

  const navigate = useNavigate();

  //console.log("Category Name:  ",catName);
  

  const categoryName=catName;

  useEffect(() => {
    const fetchForms = async () => {
      try {
        const response = await axios.get(`${Http}/admin-dashboard`, {
          params: { limit: 80 },
        });

        const newsData = response.data;
        const formsArray = Array.isArray(newsData.forms) ? newsData.forms : [];
        const deshCategoryForms = formsArray.filter(form => form.category === categoryName);

        setForms(deshCategoryForms.slice(0, 8));
        setLoading(false);
      } catch (error) {
        console.error("Error Fetching Data:", error);
        setError(error.message);
        setLoading(false);
      }
    };

    fetchForms();
  }, [categoryName]);

  if (loading) {
    return <div>Loading...</div>;
  }

  if (error) {
    return <div>Error: {error}</div>;
  }

  const removeImageTags = (html) => {
    return html.replace(/<img[^>]*>/g, '');
  };

  const truncateText = (text, wordLimit) => {
    const words = text.split(' ');
    if (words.length > wordLimit) {
      return words.slice(0, wordLimit).join(' ') + '...';
    }
    return text;
  };

  const handleCarouselClick = (id) => {
    navigate(`/news/${id}`);
  };

  return (
   <HomeLayoutAlternative>
       <div id="services" className="services-container mx-5">
      <section className="services-section pb-0 mb-0">
        <div className="section-header pb-0 mb-0">
          <h2 className="section-title pb-0 mb-0">{categoryName}</h2>
          <div className="section-divider pt-0 mt-0"></div>
        </div>

        <div className="cards-container">
          {forms.length > 0 ? (
            forms.map(form => (
              <div key={form._id} className="card" onClick={() => handleCarouselClick(form._id)}>
                <img alt="card img" className="card-img" src={form.picture} />
                <div className="card-content">
                  <h2 className="card-title" dangerouslySetInnerHTML={{ __html: DOMPurify.sanitize(form.title) }}></h2>
                  <p className="card-text"
                    dangerouslySetInnerHTML={{ __html: DOMPurify.sanitize(truncateText(removeImageTags(form.content), 30)) }}
                  ></p>
                </div>
              </div>
            ))
          ) : (
            <p>No data available for the দেশ category.</p>
          )}
        </div>
      </section>
    </div>
   </HomeLayoutAlternative>
  );
};

export default CategoryPost;

